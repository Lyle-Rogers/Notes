# Black is a code formatter for python. It is the most recommended 1.
# https://pypi.org/project/black/

# pip install black: Will install the dependency.

# To use black, write black {directory/ or file.py} in the terminal and then black will format that directory.

# To format the directory you are in, like django's root directory for example, you can use black the-directory-your-in/

# Customizing blacks code formats is practically impossible. Leave it alone.